package com.cafe.crawl.model;

public class Pagination {
	private int fullPage;
	private int presentPage;
	private int cnt;
	public int getFullPage() {
		return fullPage;
	}
	public void setFullPage(int fullPage) {
		this.fullPage = fullPage;
	}
	public int getPresentPage() {
		return presentPage;
	}
	public void setPresentPage(int presentPage) {
		this.presentPage = presentPage;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	
	
}
